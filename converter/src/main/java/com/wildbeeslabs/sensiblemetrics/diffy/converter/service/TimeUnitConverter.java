package com.wildbeeslabs.sensiblemetrics.diffy.converter.service;

import com.wildbeeslabs.sensiblemetrics.diffy.common.exception.IllegalFormatConversionException;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.ToString;

import java.time.temporal.ChronoUnit;
import java.util.concurrent.TimeUnit;

/**
 * Default time unit {@link AbstractConverter} implementation
 *
 * @author Alexander Rogalskiy
 * @version 1.1
 * @since 1.0
 */
@Data
@EqualsAndHashCode(callSuper = true)
@ToString(callSuper = true)
public class TimeUnitConverter extends AbstractConverter<TimeUnit, ChronoUnit> {

    @Override
    public ChronoUnit valueOf(final TimeUnit timeUnit) {
        switch (timeUnit) {
            case NANOSECONDS:
                return ChronoUnit.NANOS;
            case MICROSECONDS:
                return ChronoUnit.MICROS;
            case MILLISECONDS:
                return ChronoUnit.MILLIS;
            case SECONDS:
                return ChronoUnit.SECONDS;
            case MINUTES:
                return ChronoUnit.MINUTES;
            case HOURS:
                return ChronoUnit.HOURS;
            case DAYS:
                return ChronoUnit.DAYS;
            default:
                throw new IllegalFormatConversionException("ERROR: no ChronoUnit equivalent for", TimeUnit.class, timeUnit);
        }
    }
}
